    private void btnConformarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConformarActionPerformed
        
        String newPass = txtNewPassword.getText();
        String repPass = txtRepitPassword.getText();
        
        if (newPass.equals(repPass)) {
            JOptionPane.showMessageDialog(this, "Cambio exitoso", "Actualizacion contraseña", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Las contrteaseñas no coinciden", "Actualizacion contraseña", JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_btnConformarActionPerformed

